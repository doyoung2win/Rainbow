import UIKit

class ViewController: UIViewController {


    @IBAction func turnBackgroundRed(_ sender: Any) {
        view.backgroundColor = .red
    }
    
    @IBAction func turnBackgroundOrange(_ sender: Any) {
        view.backgroundColor = .orange
    }
    
    
    @IBAction func turnBackgroundYellow(_ sender: Any) {
        view.backgroundColor = .yellow
    }
    
    
    @IBAction func turnBackgroundGreen(_ sender: Any) {
        view.backgroundColor = .green
    }
    
    
    @IBAction func turnBackgroundBlue(_ sender: Any) {
        view.backgroundColor = .blue
    }
    
    
    @IBAction func turnBackgroundIndigo(_ sender: Any) {
        view.backgroundColor = .magenta
    }
    

}

